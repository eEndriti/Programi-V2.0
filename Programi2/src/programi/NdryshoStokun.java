     public void data() throws SQLException{
        
        Statement s = prg.con().createStatement();
        
        ResultSet rs = s.executeQuery("select p.* , k.Emri from Produkti p , Kategoria k where p.KategoriaID = k.KategoriaID and p.ProduktiID = "+id);
        
        while(rs.next()){
            
            p = rs.getString(2);
            per = rs.getString(3);
            ss = rs.getString(4);
            cs = rs.getString(5);
            fa = rs.getString(7);
            fu = rs.getString(6);
            kategoriaId = rs.getInt(9);
            cf = rs.getString(10);
        }
        produkti.setText(p);
        pershkrimi.setText(per);
        sasia.setText(ss);
        cmimis.setText(cs);
        cmimif.setText(cf);
        fatura.setText(fa);
        furnitori.setText(fu);
        
        
        if (kategoriaId != -1 ) {
            kategorite();
        }
     
    }
     public void kategorite(){
         
         try{
             
            
            Statement s = prg.con().createStatement();
            ResultSet rs = s.executeQuery("Select Emri,KategoriaID from Kategoria");
            while(rs.next()){
                choice1.add(rs.getString(1));
                kgId.add(rs.getString(2));
                
            }
        }catch(Exception e){
            e.printStackTrace();
        }
         
            String kgS = Integer.toString(kategoriaId);
         for (int i = 0; i < kgId.size(); i++) {
             
             if (kgS.equals(kgId.get(i))) {
                choice1.select(i);
             }
         }
        
     }
