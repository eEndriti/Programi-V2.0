    public long llogaritDitet(String dataSkadimit){
        
        String dataAktuale = data();       
        LocalDate l = LocalDate.parse(dataAktuale);
        LocalDate s = LocalDate.parse(dataSkadimit);
        
        long t= ChronoUnit.DAYS.between(l,s);
        return t;
    }
    public String data(){
        LocalDate dateObj = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String date = dateObj.format(formatter);
        return date;
    }
