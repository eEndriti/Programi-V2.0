     public void search(){
        
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        TableRowSorter sorter = new TableRowSorter<>(dt);
        jTable1.setRowSorter(sorter);
        
        searchField.getDocument().addDocumentListener(new DocumentListener() {
         @Override
         public void insertUpdate(DocumentEvent e) {
            search(searchField.getText());
         }
         @Override
         public void removeUpdate(DocumentEvent e) {
            search(searchField.getText());
         }
         @Override
         public void changedUpdate(DocumentEvent e) {
            search(searchField.getText());
         }
         public void search(String str) {
            if (str.length() == 0) {
               sorter.setRowFilter(null);
            } else {
               sorter.setRowFilter(RowFilter.regexFilter("(?i)" + str));
            }
         }
      });
     }
    public void loadtable(){
         DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        try{
            Statement s = prg.con().createStatement();
            ResultSet rs = s.executeQuery("Select * from transaksionet");
            while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(7));
                v.add(rs.getString(3));
                v.add(rs.getString(4)+" €");
                v.add(rs.getString(5));
                v.add(rs.getString(6));
                if (rs.getString(2).equals("Shitje")) {
                    String dataSkadimit = rs.getString(8);
                    String dataGarancionit = rs.getString(5);
                    long ditet = llogaritDitet(dataSkadimit);
                    if (ditet == 0 || ditet < 0) {
                        v.add("I Skaduar");
                    }else
                        v.add("Ditet e mbetura: "+ditet);
                }
                
                dt.addRow(v);
                
            }
        }catch(SQLException e){
            e.printStackTrace();
        }
    }
