    private void jButton3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MousePressed
        alert7.setText("");
        
        String date = tt();
       
        
        if (jTable1.getRowCount() == 0) {
            alert7.setText("Nuk ka asnje produkt per tu shtuar!");
        }else{
            try {
                Statement s = prg.con().createStatement();
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    Vector v = new Vector();
                    for (int j = 0; j < jTable1.getColumnCount(); j++) {
                        v.add(jTable1.getValueAt(i, j));
                    }
                     
                     s.execute("Insert into Produkti (Emri,Pershkrimi,Sasia,Cmimi,Furnitori,Fatura,DataRegjistrimit,KategoriaID,CmimiFurnizimit) values ('"+v.get(1)+"','"+v.get(2)+"','"+v.get(3)+"','"+v.get(5)+"','"+v.get(7)+"','"+v.get(6)+"','"+date+"','"+v.get(8)+"','"+v.get(4)+"')");
                     
                }
            } catch (SQLException ex) {
                alert7.setText("Fatura nuk mund te shtohet");
            }
            alert7.setText("Fatura u shtua me sukses");
                     DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                     dtm.setRowCount(0);
                     produkti.setText("");
                     pershkrimi.setText("");
                     sasia.setText("");
                     cmimif.setText("");
                     fatura.setText("");
                     furnitori.setText("");
                     cmimis.setText("");
                     
                     
                    
        }
    }//GEN-LAST:event_jButton3MousePressed
    public String tt() {
        LocalDate dateObj = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String date = dateObj.format(formatter);
        return date;
    }
