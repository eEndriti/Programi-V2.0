    public void check(){
        alert1.setText("");
        alert4.setText("");
        if (produkti.getText() == null || produkti.getText().trim().isEmpty()) {
            alert1.setText("!");
            return;
        }if (cmimif.getText() == null || cmimif.getText().trim().isEmpty()) {
            alert4.setText("!");
            return;
        }
        shtoNeTabele();
    }
    public void shtoNeTabele(){
        
        Vector v = new Vector();
        v.add(nrRreshtit);
        v.add(produkti.getText());
        v.add(pershkrimi.getText());
        v.add(sasia.getText());
        v.add(cmimif.getText());
        v.add(cmimis.getText());
        v.add(fatura.getText());
        v.add(furnitori.getText());
        int k = choice1.getSelectedIndex();
        v.add(id.get(k));
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.addRow(v);
        nrRreshtit++;
    }
