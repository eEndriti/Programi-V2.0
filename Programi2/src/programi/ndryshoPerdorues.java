    public void check(){
        if (epField.getText() == null || epField.getText().trim().isEmpty()) {
            alert1.setText("!");
        }
        
        if (pField.getText() == null || pField.getText().trim().isEmpty()) {
            alert2.setText("!");
        }
        ndrysho();
    }
    public void ndrysho(){
        try{
            int sasia = choice1.getItemCount();
            Statement s = prg.con().createStatement();
            s.execute("Update useri set emriPerdoruesit = '"+epField.getText()+"', passwordi = '"+pField.getText()+"',emriFirmes = '"+emriBField.getText()+"'"
                    + ", nrBiznesit = '"+nrBField.getText()+"', Adresa = '"+adField.getText()+"',nrFiskal = '"+nrFField.getText()+"',nrTelefonit = '"+nrTField.getText()+"',Xhirollogaria = '"+sasia+"' where userID = '"+useriId+"'");
            
            s.execute("delete from xhirot where userId = "+useriId);
            
            for (int i = 0; i < choice1.getItemCount(); i++) {
                s.execute("Insert into xhirot(Xhirollogaria,userId) values('"+choice1.getItem(i)+"','"+useriId+"')");
            }
            
            Parametrat p = new Parametrat();
            Parametrat.alert3.setText("Ndryshimet u ruajten me sukses!");
            p.setVisible(true);
            dispose();
            
        }catch(SQLException e){
            e.printStackTrace();
        }
    }
