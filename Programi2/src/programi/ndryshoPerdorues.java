    private void plusMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_plusMousePressed
        boolean x = true;
        if ( shtoX.getText() == null || shtoX.getText().trim().isEmpty()) {
            alert3.setText("!");
            x = false;
        }
        for (int i = 0; i < choice1.getItemCount(); i++) {
            if (shtoX.getText().equals(choice1.getItem(i))) {
                alert3.setText("!");
                x = false;
            }
        }
        if (x) {
            choice1.add(shtoX.getText());
        }
        
    }//GEN-LAST:event_plusMousePressed

